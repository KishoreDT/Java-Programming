public class Alpha {
    public static void main(String[] args) {
        System.out.println("Alphabats in Lowercases :");
        for (char ch='a';ch<='z';ch++) {
            System.out.printf("%s ",ch);
        }
        System.out.println("\n");
        System.out.println("Alphabats in Uppercases :");
        for (char ch='A';ch<='Z';ch++) {
            System.out.printf("%s ",ch);
        }
    }
}